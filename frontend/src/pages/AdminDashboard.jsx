// frontend/src/pages/AdminDashboard.jsx

import React, { useEffect, useState } from "react";

import {
  FaUsers,
  FaHeartbeat,
  FaTint,
  FaNotesMedical,
  FaChartLine,
  FaUserShield,
  FaArrowUp,
} from "react-icons/fa";

import HealthCard from "../components/HealthCard";
import ChartCard from "../components/ChartCard";

import api from "../api/client";

import "./AdminDashboard.css";

const AdminDashboard = () => {
  const [stats, setStats] = useState(null);

  const [recent, setRecent] = useState([]);

  const [error, setError] = useState(null);

  const [loading, setLoading] = useState(true);

  // ======================================================
  // FETCH ADMIN STATS
  // ======================================================

  useEffect(() => {
    const fetchStats = async () => {
      try {
        const res = await api.get("/admin/stats");

        setStats(res.data);

        setRecent(res.data.recent_predictions || []);
      } catch (err) {
        console.error(err);

        setError(
          err.response?.data?.message || "Failed to load admin statistics.",
        );
      } finally {
        setLoading(false);
      }
    };

    fetchStats();
  }, []);

  // ======================================================
  // LOADING
  // ======================================================

  if (loading) {
    return (
      <div className="admin-dashboard">
        <div className="admin-loading">
          <div className="loader"></div>

          <p>Loading admin analytics...</p>
        </div>
      </div>
    );
  }

  // ======================================================
  // ERROR
  // ======================================================

  if (error) {
    return (
      <div className="admin-dashboard">
        <div className="error">{error}</div>
      </div>
    );
  }

  return (
    <div className="admin-dashboard">
      {/* HEADER */}
      <div className="admin-header">
        <div>
          <h1>Admin Analytics Dashboard</h1>

          <p>
            Monitor users, disease predictions, platform activity, and
            healthcare insights across the system.
          </p>
        </div>

        <div className="admin-badge">
          <FaUserShield />

          <span>Administrator</span>
        </div>
      </div>

      {/* STATS */}
      <div className="admin-stats">
        <HealthCard
          title="Total Users"
          level="Low"
          percentage={stats?.total_users || 0}
          description="Registered platform users."
        />

        <HealthCard
          title="Total Predictions"
          level="Medium"
          percentage={stats?.total_predictions || 0}
          description="Predictions generated by AI."
        />

        <HealthCard
          title="Heart Predictions"
          level="High"
          percentage={stats?.heart_predictions || 0}
          description="Heart disease predictions."
        />

        <HealthCard
          title="Diabetes Predictions"
          level="Medium"
          percentage={stats?.diabetes_predictions || 0}
          description="Diabetes prediction requests."
        />

        <HealthCard
          title="Kidney Predictions"
          level="Low"
          percentage={stats?.kidney_predictions || 0}
          description="Kidney disease predictions."
        />
      </div>

      {/* ANALYTICS GRID */}
      <div className="admin-grid">
        {/* LEFT */}
        <div className="admin-grid-left">
          <ChartCard
            title="Prediction Analytics"
            subtitle="Overview of AI prediction activity across diseases."
            trend="+18% prediction growth this month"
          >
            <div className="analytics-placeholder">
              <div className="analytics-row">
                <div className="analytics-icon heart">
                  <FaHeartbeat />
                </div>

                <div className="analytics-content">
                  <h4>Heart Disease Analysis</h4>

                  <p>{stats?.heart_predictions} total predictions</p>
                </div>
              </div>

              <div className="analytics-row">
                <div className="analytics-icon diabetes">
                  <FaTint />
                </div>

                <div className="analytics-content">
                  <h4>Diabetes Analysis</h4>

                  <p>{stats?.diabetes_predictions} total predictions</p>
                </div>
              </div>

              <div className="analytics-row">
                <div className="analytics-icon kidney">
                  <FaNotesMedical />
                </div>

                <div className="analytics-content">
                  <h4>Kidney Disease Analysis</h4>

                  <p>{stats?.kidney_predictions} total predictions</p>
                </div>
              </div>
            </div>
          </ChartCard>

          <ChartCard
            title="Platform Growth"
            subtitle="Healthcare system usage insights."
            trend="+12% active users"
          >
            <div className="growth-grid">
              <div className="growth-card">
                <FaUsers />

                <h3>{stats?.total_users || 0}</h3>

                <p>Registered Users</p>
              </div>

              <div className="growth-card">
                <FaChartLine />

                <h3>{stats?.total_predictions || 0}</h3>

                <p>AI Predictions</p>
              </div>

              <div className="growth-card">
                <FaArrowUp />

                <h3>98%</h3>

                <p>System Accuracy</p>
              </div>
            </div>
          </ChartCard>
        </div>

        {/* RIGHT */}
        <div className="admin-grid-right">
          <div className="admin-insights-card">
            <h3>AI Insights</h3>

            <div className="insight-item">
              <div className="insight-dot"></div>

              <p>
                Heart disease predictions are the highest among all disease
                categories.
              </p>
            </div>

            <div className="insight-item">
              <div className="insight-dot"></div>

              <p>Platform engagement increased significantly this month.</p>
            </div>

            <div className="insight-item">
              <div className="insight-dot"></div>

              <p>
                More users are actively using preventive healthcare analytics.
              </p>
            </div>
          </div>

          <div className="admin-insights-card">
            <h3>System Status</h3>

            <div className="system-status">
              <span>API Status</span>

              <div className="status-badge online">Online</div>
            </div>

            <div className="system-status">
              <span>ML Models</span>

              <div className="status-badge online">Active</div>
            </div>

            <div className="system-status">
              <span>Database</span>

              <div className="status-badge online">Healthy</div>
            </div>
          </div>
        </div>
      </div>

      {/* TABLE */}
      <section className="admin-table-section">
        <div className="table-header">
          <h2>Recent Predictions</h2>

          <p>Latest disease prediction activity across users.</p>
        </div>

        <div className="table-wrapper">
          <table className="admin-table">
            <thead>
              <tr>
                <th>User ID</th>

                <th>Disease</th>

                <th>Risk Level</th>

                <th>Risk %</th>

                <th>Date</th>
              </tr>
            </thead>

            <tbody>
              {recent.length > 0 ? (
                recent.map((p) => (
                  <tr key={p.id}>
                    <td>#{p.user_id}</td>

                    <td>
                      <span className="disease-badge">{p.disease}</span>
                    </td>

                    <td>
                      <span
                        className={`risk-badge ${
                          p.risk_level?.toLowerCase() || ""
                        }`}
                      >
                        {p.risk_level}
                      </span>
                    </td>

                    <td>{p.risk_percentage}%</td>

                    <td>{p.created_at}</td>
                  </tr>
                ))
              ) : (
                <tr>
                  <td colSpan="5" className="no-data">
                    No recent predictions found.
                  </td>
                </tr>
              )}
            </tbody>
          </table>
        </div>
      </section>
    </div>
  );
};

export default AdminDashboard;
